
public class Calculator {

	public static void main(String[] args) {
		
		int a = 7;
		
		int b = 3;
		
		System.out.println(a + b - 12);
		
		System.out.println(a * b);
		
		System.out.println(a / b); // 取整數「商數」
		
		System.out.println(a % b); // 取餘數
		
		System.out.println(a + b * 12);
		
		System.out.println(   (a + b) * 12 );
		
		System.out.println(a + b / 12);
	}

}
